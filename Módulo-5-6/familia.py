"""Uma familia tem 3 pessoas. Pretende um programa que utiliza um dicionário para registar o nome de 
cada um e a marca do respetivo telemóvel e computador.
O programa deve indicar qual a marca mais comum e se algum membro da familia tem somente uma marca"""

dicionario={}
for i in range(3):
    nome=input("Introduza o nome: ")
    marca_telemóvel=input("Introduza a marca do telemóvel: ")
    marca_computador=input("Introduza a marca do computador: ")
    dicionario[nome]={"Marca telemóvel": marca_telemóvel,"Marca computador":marca_computador}



    




    
        
    
    
    


